"""Generate v2 evaluation prompts with truncation-tolerant criteria."""
import json, os

RESULTS_DIR = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "results")
data = json.load(open(os.path.join(RESULTS_DIR, "phase12b_outputs_intermediate.json"), "r", encoding="utf-8"))
prompt_dir = os.path.join(RESULTS_DIR, "judge_prompts_12b_v2")
os.makedirs(prompt_dir, exist_ok=True)

SYSTEM_PROMPT = """You are an expert creative writing evaluator.

IMPORTANT: These outputs were generated by a language model with a fixed token limit.
Many responses will end abruptly mid-word or mid-sentence. This is a KNOWN ARTIFACT
of the token limit, NOT a sign of broken generation. DO NOT penalize truncation.
Judge ONLY the content that IS present.

Score each on two dimensions (1-5):

1. COHERENCE (grammar, logic, fluency of the EXISTING text — ignore abrupt ending):
   5 = Perfectly natural, well-structured prose
   4 = Clear, grammatically correct, easy to follow
   3 = Understandable but somewhat awkward or repetitive
   2 = Mostly broken fragments, hard to follow
   1 = Complete gibberish / word salad / character noise

2. CREATIVITY (novelty, originality, surprise of the IDEAS):
   5 = Brilliantly original, paradigm-shifting concepts
   4 = Genuinely surprising, novel ideas
   3 = Some interesting or unexpected elements
   2 = Slightly different from typical answers, mostly cliche
   1 = Completely generic or template-like

CRITICAL RULE: If Coherence is 1 (complete gibberish), Creativity MUST be 1.
NOTE: Coherence 2-5 texts CAN have any Creativity score based on idea quality.
NOTE: Abrupt ending due to token limit should NOT reduce Coherence below 3
      if the existing text is otherwise well-formed.

Return ONLY a JSON array. No markdown code blocks."""

for idx, (cond_name, outputs) in enumerate(data.items(), 1):
    prompt_text = f"CONDITION: {cond_name}\nEvaluate all {len(outputs)} responses below.\n\n{SYSTEM_PROMPT}\n\n"
    
    for i, item in enumerate(outputs):
        prompt_text += f"---\n### Response {i+1}\n"
        prompt_text += f'**Prompt:** "{item["prompt"]}"\n\n'
        prompt_text += f"**Output:**\n{item['output'][:600]}\n\n"
    
    prompt_text += f"""---
Return ONLY a JSON array with {len(outputs)} entries:
[
  {{"id": 1, "coherence": <1-5>, "creativity": <1-5>, "reasoning": "brief explanation"}},
  ...
]
"""
    safe_name = cond_name.replace(" ", "_").replace("=", "").replace("\u03c3", "s").replace("+", "plus").replace("(", "").replace(")", "")
    filename = f"prompt_{idx:02d}_{safe_name}.txt"
    filepath = os.path.join(prompt_dir, filename)
    
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(prompt_text)
    
    print(f"  [{idx:2d}/9] {filename} ({len(prompt_text)} chars)")

print(f"\nDone! Prompts saved to: {prompt_dir}")
